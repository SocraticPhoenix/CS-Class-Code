public class Warehouse {
    private int x;
    private int y;
    private int number;

    public Warehouse(int x, int y, int number) {
        this.x = x;
        this.y = y;
        this.number = number;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public int getNumber() {
        return this.number;
    }

}
